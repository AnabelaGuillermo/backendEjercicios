import express from 'express';
import Task from '../../models/taskModel.js';

const taskRouter = express.Router();

// Crear una nueva tarea
taskRouter.post('/', async (req, res) => {
  try {
    const task = new Task(req.body);
    await task.save();
    res.status(201).send(task);
  } catch (error) {
    res.status(400).send(error);
  }
});

// Listar todas las tareas
taskRouter.get('/', async (req, res) => {
  try {
    const tasks = await Task.find();
    res.status(200).send(tasks);
  } catch (error) {
    res.status(500).send(error);
  }
});

// Obtener una tarea por ID
taskRouter.get('/:id', async (req, res) => {
  try {
    const task = await Task.findById(req.params.id);
    if (!task) {
      return res.status(404).send();
    }
    res.status(200).send(task);
  } catch (error) {
    res.status(500).send(error);
  }
});

// Borrar una tarea por ID
taskRouter.delete('/:id', async (req, res) => {
  try {
    const task = await Task.findByIdAndDelete(req.params.id);
    if (!task) {
      return res.status(404).send();
    }
    res.status(200).send(task);
  } catch (error) {
    res.status(500).send(error);
  }
});

// Editar una tarea por ID
taskRouter.patch('/:id', async (req, res) => {
  try {
    const task = await Task.findByIdAndUpdate(req.params.id, req.body, {
      new: true,
      runValidators: true,
    });
    if (!task) {
      return res.status(404).send();
    }
    res.status(200).send(task);
  } catch (error) {
    res.status(400).send(error);
  }
});

// Obtener una tarea por nombre o ID
taskRouter.get('/search', async (req, res) => {
  try {
    const { name, id } = req.query;
    let task;
    if (id) {
      task = await Task.findById(id);
    } else if (name) {
      task = await Task.findOne({ name });
    } else {
      return res.status(400).send('ParÃ¡metros de consulta faltantes.');
    }
    if (!task) {
      return res.status(404).send();
    }
    res.status(200).send(task);
  } catch (error) {
    res.status(500).send(error);
  }
});

export default taskRouter;
